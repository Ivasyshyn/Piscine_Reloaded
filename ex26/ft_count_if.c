int		ft_count_if(char **tab, int (*f)(char*))
{
	int counter;
	int x;

	x = 0;
	counter = 0;
	while (tab[x] != 0)
	{
		if (f(tab[x]) == 1)
			counter++;
		x++;
	}
	return (counter);
}
